#!/usr/bin/env node
/**
 * ucuf-gen-rule-test.js — UCUF 規則測試骨架自動生成器
 *
 * 用法：
 *   node tools_node/ucuf-gen-rule-test.js --rule-id RT-11
 *   node tools_node/ucuf-gen-rule-test.js --rule-id RT-11 --out tests/ucuf/RT-11.test.ts
 *
 * 功能：
 *   1. 從 ucuf-rules-registry.json 讀取指定 rule-id 的規則定義
 *   2. 生成包含 trigger / no-trigger 兩個 suite.test() 骨架的 TypeScript 測試檔
 *   3. 若指定 --out，寫入檔案；否則印至 stdout
 *
 * 格式與 tests/ucuf/scaffoldV2.test.ts 保持一致。
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const REGISTRY_PATH = path.resolve(
    __dirname,
    '../assets/resources/ui-spec/ucuf-rules-registry.json',
);

function parseArgs(argv) {
    const args = { ruleId: '', out: '' };
    for (let i = 0; i < argv.length; i++) {
        if (argv[i] === '--rule-id' && argv[i + 1]) {
            args.ruleId = argv[i + 1];
            i++;
        } else if (argv[i] === '--out' && argv[i + 1]) {
            args.out = argv[i + 1];
            i++;
        }
    }
    return args;
}

function loadRegistry() {
    if (!fs.existsSync(REGISTRY_PATH)) {
        throw new Error(`找不到規則資料庫：${REGISTRY_PATH}`);
    }
    return JSON.parse(fs.readFileSync(REGISTRY_PATH, 'utf-8'));
}

function generateTestSkeleton(rule) {
    const { id, name, severity, triggerPoints } = rule;
    const triggerList = (triggerPoints || []).join(', ') || '(none)';
    const className = `${id.replace('-', '')}Test`;

    return `import { TestSuite } from '../TestSuite';
import * as assert from 'assert';

/**
 * ${id} — ${name}
 *
 * severity:      ${severity}
 * triggerPoints: ${triggerList}
 *
 * Auto-generated by ucuf-gen-rule-test.js — fill in the test bodies.
 */
export function create${className}Suite(): TestSuite {
    const suite = new TestSuite('${id}: ${name}');

    // Scenario 1: rule PASSES (compliant input)
    suite.test('${id} passes when constraint is satisfied', () => {
        // TODO: construct a compliant context and call the rule's checkFn or RuntimeRuleChecker static method
        // e.g.  const result = RuntimeRuleChecker.checkRT??(...);
        //       assert.strictEqual(result.passed, true);
        assert.ok(true, 'Replace this stub with a real assertion');
    });

    // Scenario 2: rule FAILS (violation input)
    suite.test('${id} fails when constraint is violated', () => {
        // TODO: construct a violating context and assert passed === false
        // e.g.  const result = RuntimeRuleChecker.checkRT??(...);
        //       assert.strictEqual(result.passed, false);
        //       assert.strictEqual(result.ruleId, '${id}');
        assert.ok(true, 'Replace this stub with a real assertion');
    });

    return suite;
}
`;
}

function main() {
    const args = parseArgs(process.argv.slice(2));

    if (!args.ruleId) {
        console.error('用法: node tools_node/ucuf-gen-rule-test.js --rule-id <ruleId> [--out <path>]');
        process.exit(1);
    }

    let registry;
    try {
        registry = loadRegistry();
    } catch (err) {
        console.error(`[ucuf-gen-rule-test] ${err.message}`);
        process.exit(1);
    }

    const rule = (registry.rules || []).find(r => r.id === args.ruleId);
    if (!rule) {
        // Rule not in registry yet — generate a placeholder skeleton anyway
        console.warn(`[ucuf-gen-rule-test] 警告：'${args.ruleId}' 不在 registry 中，使用預設佔位符`);
        const placeholder = {
            id: args.ruleId,
            name: 'TODO_RULE_NAME',
            severity: 'warning',
            triggerPoints: [],
        };
        const skeleton = generateTestSkeleton(placeholder);
        if (args.out) {
            const outPath = path.resolve(process.cwd(), args.out);
            const dir = path.dirname(outPath);
            if (!fs.existsSync(dir)) { fs.mkdirSync(dir, { recursive: true }); }
            fs.writeFileSync(outPath, skeleton, 'utf-8');
            console.log(`[ucuf-gen-rule-test] 已寫入：${outPath}`);
        } else {
            process.stdout.write(skeleton);
        }
        return;
    }

    const skeleton = generateTestSkeleton(rule);

    if (args.out) {
        const outPath = path.resolve(process.cwd(), args.out);
        const dir = path.dirname(outPath);
        if (!fs.existsSync(dir)) { fs.mkdirSync(dir, { recursive: true }); }
        fs.writeFileSync(outPath, skeleton, 'utf-8');
        console.log(`[ucuf-gen-rule-test] 已寫入：${outPath}`);
    } else {
        process.stdout.write(skeleton);
    }
}

main();
