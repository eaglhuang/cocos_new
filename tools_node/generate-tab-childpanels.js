#!/usr/bin/env node
// doc_id: doc_other_0009 — Generate Tab ChildPanels (M33)
//
// Purpose:
//   For a screen with N tabs (declared either in the layout JSON or via
//   --tabs flag), produce N ChildPanel TypeScript scaffolds + a
//   `<screen>.tab-routing.json` sidecar so the CompositePanel can route
//   tab switches without hand-coding each child.
//
// Usage:
//   node tools_node/generate-tab-childpanels.js \
//     --layout assets/resources/ui-spec/layouts/character-ds3-main.json \
//     --tabs Overview,Stats,Tactics,Bloodline,Equip,Aptitude \
//     --component-prefix CharacterDs3 \
//     --out-dir assets/scripts/ui/components/character-ds3 \
//     [--dry-run]
//
// Output:
//   - {out-dir}/{Prefix}{Tab}Child.ts     (one per tab, idempotent: skips existing)
//   - {screens-dir}/{screen}.tab-routing.json
//
// Notes:
//   - This is a SCAFFOLD generator. Existing ChildPanel files are NEVER
//     overwritten; only missing files are created. Use to seed new screens.
//   - For multi-tab production work, prefer Phase B5 (migration plan) to
//     attach these to a CompositePanel.

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SCREENS_DIR = path.join(ROOT, 'assets/resources/ui-spec/screens');

function parseArgs(argv) {
  const opts = { layout: null, tabs: null, componentPrefix: null, outDir: null, dryRun: false };
  for (let i = 2; i < argv.length; i += 1) {
    const a = argv[i];
    const next = () => argv[++i];
    switch (a) {
      case '--layout': opts.layout = next(); break;
      case '--tabs': opts.tabs = next().split(',').map(s => s.trim()).filter(Boolean); break;
      case '--component-prefix': opts.componentPrefix = next(); break;
      case '--out-dir': opts.outDir = next(); break;
      case '--dry-run': opts.dryRun = true; break;
      case '--help':
      case '-h':
        console.log('Usage: generate-tab-childpanels.js --layout <json> --tabs <csv> --component-prefix <Prefix> --out-dir <ts-dir> [--dry-run]');
        process.exit(0);
        break;
      default:
        console.error(`[generate-tab-childpanels] unknown arg: ${a}`); process.exit(2);
    }
  }
  if (!opts.layout || !opts.tabs || !opts.componentPrefix || !opts.outDir) {
    console.error('[generate-tab-childpanels] --layout, --tabs, --component-prefix, --out-dir are required');
    process.exit(2);
  }
  return opts;
}

function buildChildPanelTs(prefix, tab) {
  const className = `${prefix}${tab}Child`;
  const rootPath = `Tab${tab}Content`;
  return `// doc_id: doc_other_0009 — generated by generate-tab-childpanels.js (M33)
//
// ${className} — UCUF ChildPanel for tab "${tab}".
// Wire this into the CompositePanel's _switchToTab routing table.

import { ChildPanelBase } from '../../core/ChildPanelBase';
import type { UISkinResolver } from '../../core/UISkinResolver';
import type { UITemplateBinder } from '../../core/UITemplateBinder';
import { UCUFLogger, LogCategory } from '../../core/UCUFLogger';

export class ${className} extends ChildPanelBase {
    override dataSource = 'config';
    private static readonly ROOT_PATH = '${rootPath}';

    public override onMount(binder: UITemplateBinder, _skin: UISkinResolver): void {
        UCUFLogger.info(LogCategory.LIFECYCLE, '[${className}] onMount', { rootPath: ${className}.ROOT_PATH });
        // TODO: cache Label / Sprite refs from binder under ${rootPath}
        void binder;
    }

    public override onDataChanged(_data: unknown): void {
        // TODO: bind data to cached refs.
    }

    public override onUnmount(): void {
        // TODO: release refs.
    }
}
`;
}

function buildTabRouting(screenId, prefix, tabs) {
  return {
    screenId,
    generatedAt: new Date().toISOString(),
    tabs: tabs.map(t => ({
      id: t,
      mount: `Tab${t}Content`,
      lifecycle: 'lazy',
      childPanelClass: `${prefix}${t}Child`,
    })),
  };
}

function main() {
  const opts = parseArgs(process.argv);
  const layoutPath = path.resolve(opts.layout);
  if (!fs.existsSync(layoutPath)) { console.error(`[generate-tab-childpanels] layout not found: ${layoutPath}`); process.exit(2); }
  const screenId = path.basename(layoutPath, '.json');

  const outDir = path.resolve(opts.outDir);
  const created = [];
  const skipped = [];

  for (const tab of opts.tabs) {
    const className = `${opts.componentPrefix}${tab}Child`;
    const filePath = path.join(outDir, `${className}.ts`);
    if (fs.existsSync(filePath)) { skipped.push(className); continue; }
    if (!opts.dryRun) {
      fs.mkdirSync(outDir, { recursive: true });
      fs.writeFileSync(filePath, buildChildPanelTs(opts.componentPrefix, tab), 'utf8');
    }
    created.push({ className, filePath });
  }

  const routing = buildTabRouting(screenId, opts.componentPrefix, opts.tabs);
  const routingPath = path.join(SCREENS_DIR, `${screenId}.tab-routing.json`);
  if (!opts.dryRun) {
    fs.mkdirSync(path.dirname(routingPath), { recursive: true });
    fs.writeFileSync(routingPath, JSON.stringify(routing, null, 2) + '\n', 'utf8');
  }

  // M33b: also emit a ready-to-paste codemod snippet so the user can wire
  // _switchToTab() in the CompositePanel without writing the TS by hand.
  const codemodPath = path.join(SCREENS_DIR, `${screenId}.tab-routing-codemod.txt`);
  const importBlock = opts.tabs.map(t =>
    `import { ${opts.componentPrefix}${t}Child } from './${opts.componentPrefix}${t}Child';`
  ).join('\n');
  const switchBlock = opts.tabs.map(t =>
    `      case '${t.toLowerCase()}': return ${opts.componentPrefix}${t}Child;`
  ).join('\n');
  const codemodText = [
    `// === M33 codemod: paste into <Composite>.ts ===`,
    `// Generated by generate-tab-childpanels.js for screen "${screenId}"`,
    `// Phase 1: imports (top of file)`,
    importBlock,
    ``,
    `// Phase 2: tab → ChildPanel resolver (inside CompositePanel class)`,
    `private _resolveChildPanelForTab(tabId: string): typeof ChildPanelBase | null {`,
    `    switch (tabId) {`,
    switchBlock,
    `      default: return null;`,
    `    }`,
    `}`,
    ``,
    `// Phase 3: in _switchToTab(tabId), replace the body with:`,
    `//   const Cls = this._resolveChildPanelForTab(tabId);`,
    `//   if (!Cls) { logger.warn(\`unknown tab: \${tabId}\`); return; }`,
    `//   this.mountChild(Cls, \`Tab\${capitalize(tabId)}Content\`, { dataSource: 'config' });`,
  ].join('\n');
  if (!opts.dryRun) {
    fs.writeFileSync(codemodPath, codemodText + '\n', 'utf8');
  }

  console.log(`[generate-tab-childpanels] ${opts.dryRun ? '(dry-run) ' : ''}created ${created.length} ChildPanel(s):`);
  for (const c of created) console.log(`  + ${c.className}`);
  if (skipped.length) console.log(`[generate-tab-childpanels] skipped ${skipped.length} existing:`, skipped.join(', '));
  console.log(`[generate-tab-childpanels] tab-routing → ${path.relative(ROOT, routingPath)}`);
  console.log(`[generate-tab-childpanels] codemod hints → ${path.relative(ROOT, codemodPath)}`);
}

if (require.main === module) main();
module.exports = { buildChildPanelTs, buildTabRouting };
